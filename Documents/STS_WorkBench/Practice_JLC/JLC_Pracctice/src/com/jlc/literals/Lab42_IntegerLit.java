package com.jlc.literals;
//The decimal Literal must not start with Zero
//Octal Literal must with 0.
//HexaDecimal Literal Start with 0X/0x
//BineryLiterals Start with 0B
public class Lab42_IntegerLit {

	public static void main(String[] args) {
		byte b=65;
		short s=1234;
		int i =1234567;
		long l=23493456789L;
		int a=0234567;
		int c=0X18;
		long d=0x7956789abcdeL;
		float e=0B1000;
		
		
		
		
		
		  System.out.println(b); 
		  System.out.println(s); 
		  System.out.println(i);
		  System.out.println(l);
		  System.out.println(9876543104567L);
		  System.out.println(a);
		  System.out.println(c);
		  System.out.println(d);
		  System.out.println(e);

	}

}
